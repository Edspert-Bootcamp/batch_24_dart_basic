void main(){
  int angka1 = 10;
  int angka2 = 5;

  // angka 1 dan angka 2 sama ?
  print("Operator == ${angka1 == angka2}");

  // angka 1 dan angka 2 tidak sama ?
  print("Operator != ${angka1 != angka2}");

  // angka 1 lebih besar dari angka 2 ?
  print("Operator > ${angka1 > angka2}");

  // angka 1 lebih kecil dari angka 2 ?
  print("Operator < ${angka1 < angka2}");

  // angka 1 lebih besar atau sama dengan angka 2 ?
  print("Operator >= ${angka1 >= angka2}");

  // angka 1 lebih kecil atau sama dengan angka 2 ?
  print("Operator <= ${angka1 <= angka2}");
}